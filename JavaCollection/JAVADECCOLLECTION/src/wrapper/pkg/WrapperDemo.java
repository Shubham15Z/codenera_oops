package wrapper.pkg;

public class WrapperDemo 
{
	public static void main(String[] args) 
	{
		//Integer
		int i = 100;
		Integer intg = i;
		System.out.println(intg);
		
		//Character
		char c = 'B';
		Character ch = c;
		System.out.println(ch);
		
		//Float
		float f = 12.5f;
		Float fl = f;
		System.out.println(fl);
		
		//Double
		double d = 10.25;
		Double dl = d;
		System.out.println(dl);
		
		//Long
		long l = 123654322;
		Long l1 = l;
		System.out.println(l1);
		
		Character ch1 = 'S';
		char ch2 = ch1;
		System.out.println(ch2);
	}
}
